class Tooltip:
    """
    ĞŸÑ€Ğ¸Ğ²ÑĞ·Ñ‹Ğ²Ğ°ĞµÑ‚ Ğ²ÑĞ¿Ğ»Ñ‹Ğ²Ğ°ÑÑ‰ÑƒÑ Ğ¿Ğ¾Ğ´ÑĞºĞ°Ğ·ĞºÑƒ Ğº Ğ²Ğ¸Ğ´Ğ¶ĞµÑ‚Ñƒ.

    Ğ˜ÑĞ¿Ğ¾Ğ»ÑŒĞ·Ğ¾Ğ²Ğ°Ğ½Ğ¸Ğµ:
        Tooltip(widget, "Ğ¢ĞµĞºÑÑ‚ Ğ¿Ğ¾Ğ´ÑĞºĞ°Ğ·ĞºĞ¸")
    """
    DELAY_MS = 600
    BG       = "#2a2a2a"
    FG       = "#dddddd"
    FONT     = ("Helvetica", 11)

    def __init__(self, widget, text: str):
        self.widget = widget
        self.text   = text
        self._tip   = None
        self._job   = None
        widget.bind("<Enter>",   self._on_enter, add="+")
        widget.bind("<Leave>",   self._on_leave, add="+")
        widget.bind("<Destroy>", self._on_leave, add="+")

    def _on_enter(self, _event=None):
        self._cancel()
        self._job = self.widget.after(self.DELAY_MS, self._show)

    def _on_leave(self, _event=None):
        self._cancel()
        self._hide()

    def _cancel(self):
        if self._job:
            self.widget.after_cancel(self._job)
            self._job = None

    def _show(self):
        if self._tip:
            return
        x = self.widget.winfo_rootx() + 10
        y = self.widget.winfo_rooty() + self.widget.winfo_height() + 4
        self._tip = ctk.CTkToplevel(self.widget)
        self._tip.wm_overrideredirect(True)
        self._tip.wm_geometry(f"+{x}+{y}")
        self._tip.attributes("-topmost", True)
        ctk.CTkLabel(
            self._tip, text=self.text,
            fg_color=self.BG, text_color=self.FG,
            font=self.FONT, corner_radius=6,
            wraplength=280, justify="left",
            padx=10, pady=6,
        ).pack()

    def _hide(self):
        if self._tip:
            self._tip.destroy()
            self._tip = None


